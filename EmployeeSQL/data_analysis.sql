SELECT MANAGERS.dept_no,
DEPARTMENTS.dept_name,
MANAGERS.emp_no,
EMPLOYEE.last_name,
EMPLOYEE.first_name
FROM MANAGERS
LEFT JOIN DEPARTMENTS
ON MANAGERS.dept_no = DEPARTMENTS.dept_no
LEFT JOIN EMPLOYEE
ON MANAGERS.emp_no = EMPLOYEE.emp_no
ORDER BY MANAGERS

