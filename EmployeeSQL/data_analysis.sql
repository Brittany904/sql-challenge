SELECT EMPLOYEE.emp_no,
EMPLOYEE.last_name,
EMPLOYEE.first_name,
EMPLOYEE.sex,
EMPLOYEE.emp_no,
SALARIES.salary
FROM EMPLOYEE
LEFT JOIN SALARIES
ON EMPLOYEE.emp_no = SALARIES.emp_no

